func RandArray(v any) any {
	data := reflect.ValueOf(v)
	if data.Kind() != reflect.Array && data.Kind() != reflect.Slice && data.Kind() != reflect.String {
		return nil
	}
	return data.Index(RandInt(0, data.Len()-1)).Interface()
}

