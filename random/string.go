func RandIdNo(birthday time.Time) string {
	var id strings.Builder
	id.WriteString(RandArray(s).(string))
	id.WriteString(fmt.Sprintf("%02d", RandInt(1, 70)))
	id.WriteString(fmt.Sprintf("%02d", RandInt(1, 99)))

	id.WriteString(birthday.Format("20060102"))          // 生日码
	id.WriteString(fmt.Sprintf("%03d", RandInt(0, 999))) // 顺序码

	var weighted int
	for i, v := range id.String() {
		num, _ := strconv.Atoi(string(v))
		weighted += num * factor[i]
	}
	id.WriteString(checkCode[weighted%11])
	return id.String()
}

